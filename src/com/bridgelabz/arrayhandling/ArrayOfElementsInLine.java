// Array of Elements for each loop in line Array
package com.bridgelabz.arrayhandling;
public class ArrayOfElementsInLine {
    public static void main(String[] args) {
        int[] arr = {10, 22, 43, 45, 56, 1, 35, 33, 3, 78};
        for (int b : arr)
            System.out.print(b+" ");
    }
}
