// Increment Numbers 1 to 5 step by step

package com.bridgelabz.pattern;

public class DemoIncrementNumbers
{
    public static void main(String[] args)
    {
        for (int i =1; i<=5; i++)
        {
            System.out.println();
            for (int j=1; j<=i; j++)
            {
                System.out.print(j);
            }
        }
    }
}
